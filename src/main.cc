#include <string>
#include <vector>
#include <sstream>
#include <iostream>

/////////////////////////////////////////////////////////////////////////////////////////////////////
// function declarations
/////////////////////////////////////////////////////////////////////////////////////////////////////

void handle_command(const std::string& command);
std::vector<std::string> split(const std::string& str, char delimiter);
void usage();

/////////////////////////////////////////////////////////////////////////////////////////////////////
// main
/////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
  std::string line;
  while (true)
  {
    if (std::getline(std::cin, line))
    {
      handle_command(line);
    }
  }
  return 0;
}

